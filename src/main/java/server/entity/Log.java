package server.entity;

public class Log {
    private int loggingID;
    private String log;

    public void setLoggingID(int loggingID) { this.loggingID = loggingID; }
    public void setLog(String log) { this.log = log; }
    public int getLoggingID() { return loggingID; }
    public String getLog() { return log; }
}
